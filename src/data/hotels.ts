function makeBreakdown(rating: number): ReviewBreakdown {
  return {
    cleanliness: Math.min(9.8, rating + 0.2),
    service: Math.min(9.7, rating + 0.1),
    location: Math.min(9.9, rating + 0.3),
    amenities: Math.max(7.8, rating - 0.1),
  };
}

